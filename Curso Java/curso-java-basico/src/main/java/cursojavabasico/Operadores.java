package cursojavabasico;

public class Operadores {
    public static void main(String[] args) {
        int a = 10; // operador de atribuição
        int b = 5;
        
        b += a; // aditivo
        b -= a; // subtrativo
        b *= a; // multiplicativo
        b /= a; // diviso
        b %= a; //modular
        
        
    }
}
